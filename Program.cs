using System;
using System.Windows.Forms;

namespace GraphicsDemo
{
    // Главен клас на приложението, съдържащ входната точка.
    // Този клас е отговорен за стартирането на Windows Forms приложението.
    //
    // Студент: Dimitar Klianev
    // Факултетен номер: F112194
    static class Program
    {
        // Главната входна точка на приложението.
        // Този метод се изпълнява първи при стартиране на програмата.
        //
        // Методът извършва следните действия:
        // 1. Активира визуални стилове за модерен изглед на контролите
        // 2. Задава съвместимо рендиране на текст
        // 3. Създава и стартира главната форма на приложението
        [STAThread]
        static void Main()
        {
            // Активиране на визуални стилове за Windows XP и по-нови версии
            // Това позволява на приложението да има модерен изглед
            Application.EnableVisualStyles();
            
            // Задаване на съвместимо рендиране на текст
            // Това осигурява правилно показване на текста в различни контроли
            Application.SetCompatibleTextRenderingDefault(false);
            
            // Създаване и стартиране на главната форма
            // Application.Run() стартира главния цикъл за обработка на съобщения
            Application.Run(new MainForm());
        }
    }
}
